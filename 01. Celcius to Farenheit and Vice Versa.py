# Convert temp values back and forth between Celcius and Farenheit.
c = float(input("Enter the temperature in degree Celcius: "))
print("Temperature in degrees Farenheit is: ", 1.8*c+32)
f = float(input("Enter the temperature in degree Farenheit: "))
print("Temperature in degrees Celcius is: ", (f-32)*0.555555556)